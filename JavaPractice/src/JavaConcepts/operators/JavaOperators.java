package JavaConcepts.operators;

public class JavaOperators {

	int num = 10;
	
	public void javaOperators()
	{
		do
		{
			
			System.out.println(num);
			num--;
		}while(num>0);
		
		
		
	}
	
	
	public static void main(String[] args) {
		JavaOperators js = new JavaOperators();
		js.javaOperators();
	}
	
	
}
